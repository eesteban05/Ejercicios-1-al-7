def Suma(a, b): #Obtiene o recibe argumentos o parámetros
    suma = a + b
    return suma #return: retornar, Devolver o Regresar un valor

def Resta(a, b):
    return a - b

def Multi(a,b):
    return a * b

def Divi(a,b):
    return a / b

print(f"La suma = {Suma(10 , 5)}")
print(f"La suma = {Resta(10 , 5)}")
print(f"La suma = {Multi(10 , 5)}")
print(f"La suma = {Divi(10 , 5)}")